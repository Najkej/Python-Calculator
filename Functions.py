class Functions:
    def Add(a, b):
        return str(int(a)+int(b))
    
    def Subtract(a, b):
        return str(int(a)-int(b))
    
    def Multiply(a, b):
        return str(int(a)*int(b))
    
    def Divide(a, b):
        return str(int(a)/int(b))
    
    def Exponentiation(a, b):
        return str(int(a)**int(b))