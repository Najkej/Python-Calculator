# Funkcje matematyczne

class Functions:
    def Add(a, b):  # Dodaj
        return str(int(a)+int(b))
    
    def Subtract(a, b):  #Odejmij
        return str(int(a)-int(b))
    
    def Multiply(a, b):  #Pomnóż
        return str(int(a)*int(b))
    
    def Divide(a, b):  #Podziel
        return str(int(a)/int(b))
    
    def Exponentiation(a, b):  #Potęguj
        return str(int(a)**int(b))